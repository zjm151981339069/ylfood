package com.spfood.oms.order.utils;

/**
 * 
 *换货单状态常量
 */
public enum OrderExchangeStatus {
	
	
	CHANGE_ORDER_AUTITSTATUS(0,"待审核"),
	PASS_AUDITSTATUS(1,"审核通过"),
	NO_PASS_AUDITSTATUS(2,"审核未通过");

	private int value;
	private String name;
	private OrderExchangeStatus(int value,String name){
		this.value = value;
		this.name = name;
	}
	public int getValue() {
		return value;
	}
	
	public String getName() {
		return name;
	}
}
