package com.spfood.oms.cart.intf.domain;

import com.spfood.kernel.domain.DomainObject;

import java.util.Date;
import java.util.List;

public class Cart implements DomainObject {
	private static final long serialVersionUID = 1L;
    
    private String cartCode;

    private Long cartId;

    private String customerCode;

    private Integer cartType;

    private Date lastTime;
	
	private List<CartCommodity> commodityList;

	public String getCartCode() {
		return cartCode;
	}

	public void setCartCode(String cartCode) {
		this.cartCode = cartCode;
	}

	public Long getCartId() {
		return cartId;
	}

	public void setCartId(Long cartId) {
		this.cartId = cartId;
	}

	public String getCustomerCode() {
		return customerCode;
	}

	public void setCustomerCode(String customerCode) {
		this.customerCode = customerCode;
	}

	public Integer getCartType() {
		return cartType;
	}

	public void setCartType(Integer cartType) {
		this.cartType = cartType;
	}

	public Date getLastTime() {
		return lastTime;
	}

	public void setLastTime(Date lastTime) {
		this.lastTime = lastTime;
	}

	public List<CartCommodity> getCommodityList() {
		return commodityList;
	}

	public void setCommodityList(List<CartCommodity> commodityList) {
		this.commodityList = commodityList;
	}

	
	
	
    
}